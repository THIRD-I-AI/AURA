/**
 * Card — shadcn New-York base, retuned to AURA Terminal-Authority:
 *   • sharp corners (rounded-none) — the locked visual_direction, not rounded-xl
 *   • denser vertical rhythm (gap-4 / py-4 / px-4, not gap-6 / py-6 / px-6)
 * Colors come from the token-bound semantic aliases (bg-card, border, text-
 * card-foreground) so it themes with tokens.css. Everything else is stock.
 */
import * as React from "react"

import { cn } from "@/lib/cn"

